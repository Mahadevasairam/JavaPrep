package com.Mahadev;

public class StringDatatype {
    public static void main(String[] args) {

        String Myname ="Mahadev";
        String Myage = "18";
        String Myarea = "Karimnagar";
        System.out.println(Myname);
        System.out.println(Myage);
        System.out.println(Myarea);

        System.out.println(Myname + " " + Myage + " " +Myarea);

        Myname = Myname + Myage;
        System.out.println(Myname);

        int myage = 18;
        double myaged = 543.1234;
        System.out.println(Myage + myage + myaged);
    }
}
